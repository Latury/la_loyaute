def creer_barre_progression(
    valeur_actuelle: int,
    valeur_max: int,
    longueur: int = 10,
    caractere_rempli: str = "‚ñà",
    caractere_vide: str = "‚ñë"
) -> str:
    """
    Cr√©e une barre de progression

    Args:
        valeur_actuelle: Valeur actuelle
        valeur_max: Valeur maximale
        longueur: Longueur de la barre
        caractere_rempli: Caract√®re pour la partie remplie
        caractere_vide: Caract√®re pour la partie vide

    Returns:
        str: Barre de progression (ex: "‚ñà‚ñà‚ñà‚ñà‚ñë‚ñë‚ñë‚ñë‚ñë‚ñë 40%")
    """

    if valeur_max == 0:
        pourcentage = 0
    else:
        pourcentage = min(100, max(0, int((valeur_actuelle / valeur_max) * 100)))

    rempli = int((pourcentage / 100) * longueur)
    vide = longueur - rempli

    barre = caractere_rempli * rempli + caractere_vide * vide

    return f"{barre} {pourcentage}%"

