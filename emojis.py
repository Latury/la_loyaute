def obtenir_emoji(nom: str, fallback: str = "â—") -> str:
    """
    FONCTION 01 - obtenir_emoji
    Recupere un emoji par son nom avec fallback

    Args:
        nom: Nom de l'emoji en majuscules
        fallback: Emoji par defaut si non trouve

    Returns:
        L'emoji correspondant ou le fallback
    """
    return getattr(EMOJIS, nom.upper(), fallback)


def formater_avec_emoji(emoji: str, message: str) -> str:
    """
    FONCTION 02 - formater_avec_emoji
    Formate un message avec un emoji

    Args:
        emoji: L'emoji a utiliser (peut etre un nom ou l'emoji direct)
        message: Le message a formater

    Returns:
        Le message formate avec l'emoji
    """
    if hasattr(EMOJIS, emoji.upper()):
        emoji = getattr(EMOJIS, emoji.upper())
    return f"{emoji} {message}"


def lister_emojis() -> None:
    """
    FONCTION 03 - lister_emojis
    Liste tous les emojis disponibles par categorie
    """
    print("\n" + "=" * 70)
    print("EMOJIS DISPONIBLES")
    print("=" * 70 + "\n")

    for nom in dir(EMOJIS):
        if not nom.startswith("_") and nom.isupper():
            emoji = getattr(EMOJIS, nom)
            print(f"  {nom.ljust(20)} : {emoji}")

    print("\n" + "=" * 70 + "\n")


